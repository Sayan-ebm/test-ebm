#########################################################
#### Log-Normal Distribution ###########################
#########################################################
sample_lognormal_from_mean_se <- function(
    mean,
    se
){
  #######################################################
  #### Validation #######################################
  #######################################################
  if(
    is.null(se) ||
    is.na(se) ||
    se <= 0
  ){
    return(mean)
  }
  if(mean <= 0){
    return(mean)
  }
  #######################################################
  #### Calculate Parameters #############################
  #######################################################
  sigma2 <- log(1 + (se^2 / mean^2))
  mu <- log(mean) - sigma2 / 2
  #######################################################
  #### Sample ###########################################
  #######################################################
  rlnorm(
    n = 1,
    meanlog = mu,
    sdlog = sqrt(sigma2)
  )
}





