#########################################################
#### Normal Distribution ###############################
#########################################################
sample_normal_from_mean_se <- function(
    mean,
    se
){
  #######################################################
  #### Validation #######################################
  #######################################################
  if(
    is.null(se) ||
    is.na(se) ||
    se <= 0
  ){
    return(mean)
  }
  #######################################################
  #### Sample ###########################################
  #######################################################
  rnorm(
    n = 1,
    mean = mean,
    sd = se
  )
}

#########################################################
#### Beta Distribution #################################
#########################################################
sample_beta_from_mean_se <- function(
    mean,
    se
){
  #######################################################
  #### Validation #######################################
  #######################################################
  if(
    is.null(se) ||
    is.na(se) ||
    se <= 0
  ){
    return(mean)
  }
  if(
    mean <= 0 ||
    mean >= 1
  ){
    return(mean)
  }
  #######################################################
  #### Calculate Parameters #############################
  ######################################################
  variance <- se^2
  temp <- (mean * (1 - mean) / variance) - 1
  alpha <- mean * temp
  beta <- (1 - mean) * temp
  #######################################################
  #### Validation #######################################
  #######################################################
  if(
    alpha <= 0 ||
    beta <= 0
  ){
    return(mean)
  }
  #######################################################
  #### Sample ###########################################
  #######################################################
  rbeta(
    n = 1,
    shape1 = alpha,
    shape2 = beta
  )
}

#########################################################
#### Gamma Distribution ################################
#########################################################
sample_gamma_from_mean_se <- function(
    mean,
    se
){
  #######################################################
  #### Validation #######################################
  #######################################################
  if(
    is.null(se) ||
    is.na(se) ||
    se <= 0
  ){
    return(mean)
  }
  
  if(mean <= 0){
    return(mean)
  }
  #######################################################
  #### Calculate Parameters #############################
  #######################################################
  variance <- se^2
  shape <- mean^2 / variance
  scale <- variance / mean
  #######################################################
  #### Validation #######################################
  #######################################################
  if(
    shape <= 0 ||
    scale <= 0
  ){
    return(mean)
  }
  #######################################################
  #### Sample ###########################################
  #######################################################
  rgamma(
    n = 1,
    shape = shape,
    scale = scale
  )
}

